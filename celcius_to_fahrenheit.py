# WAP to ask user to enter temprature in celsius
celsius = float(input("Enter temprature in celsius: "))
# formula
fahrenheit = (celsius * 1.8) + 32
print("{0} degree Celsius is equivalent to: {1} degree Fahrenheit".format(celsius, fahrenheit))
